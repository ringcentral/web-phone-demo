const fs = require("node:fs");
const path = require("node:path");

const rootDir = path.resolve(__dirname, "..");
const dependencyPackages = ["ringcentral-web-phone"];

function getInstalledPackageVersion(packageName) {
  const packageJsonPath = path.join(
    rootDir,
    "node_modules",
    packageName,
    "package.json",
  );

  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
  return packageJson.version;
}

function toConstName(packageName) {
  return `${packageName.replace(/[^a-zA-Z0-9]+/g, "_").toUpperCase()}_VERSION`;
}

const generatedLines = [
  "// Auto-generated by scripts/generate-versions.js. Do not edit manually.",
  "",
];

for (const packageName of dependencyPackages) {
  const version = getInstalledPackageVersion(packageName);
  const constName = toConstName(packageName);
  generatedLines.push(`export const ${constName} = \"${version}\";`);
}

generatedLines.push("");

const outputPath = path.join(rootDir, "src", "generated", "versions.ts");
fs.writeFileSync(outputPath, generatedLines.join("\n"), "utf8");

console.log(`Generated ${path.relative(rootDir, outputPath)}`);
